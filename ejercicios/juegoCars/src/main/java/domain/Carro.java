package domain;

public class Carro {

    private int idCarro;

    private String color;

    public Carro() {
    }

    public Carro(int idCarro, String color) {
        this.idCarro = idCarro;
        this.color = color;
    }

    public int getIdCarro() {
        return idCarro;
    }

    public void setIdCarro(int idCarro) {
        this.idCarro = idCarro;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }
    
   
    
      
      

}
